public class EstruturasDeDecisao {
	public static void main(String[] args) {
		int a = 13;
		int b = 12;
		
		if (a > b) { // Se
			System.out.println("A é maior que B");
		} else if( a < b) { // se não se 
			System.out.println("A é menor que B");
		} else  { // senão
			System.out.println("A é igual a B");
		} 
		
	}
}
