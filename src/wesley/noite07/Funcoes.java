package wesley.noite07;

public class Funcoes {
	public static void facaAlgumaCoisa() {
		System.out.println("Sem parametros");
	}
	
	public static void facaAlgumaCoisa(int i) {
		System.out.println("Comparametro Int");
	}
	
	public static void facaAlgumaCoisa(int i, String s) {
		System.out.println("Comparametro Int, String");
	}
	
	public static void facaAlgumaCoisa(String s, int i) {
		System.out.println("Comparametro Strin, Int");
	}
	
	public static void facaAlgumaCoisa(String s) {
		System.out.println("Comparametro String");
	}
}
